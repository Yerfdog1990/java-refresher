package Day2;

public class DataTypes {
    static void main(String[] args) {
        // Primitive data types -> Can only store a single data at a time.
        byte b = 127;           // byte type
        short s = 32767;        // short type
        int age = 30;           // integer type
        long i = 123456789L;    // long data type -> declared with literal (L)
        float price = 10.25F;   // decimal number -> maximum 7 decimal digits -> declared with literal (F)
        double d = 3.14159;     // decimal number -> maximum 15 decimal digits
        char grade = 'A';       // single character
        boolean isPassed = true;// boolean type

    }
}
